# Day 1 - 30-days-of-python Challenge

print(2 + 3)    # addition(+)
print(3 - 1)    # substraction(-)
print(2 * 3)    # multiplication(*)
print(3 / 2)    # division(/)
print(3 ** 2)   # exponential(**)
print(3 % 2)    # modulus(%)
print(3 // 2)   # Floor division operator(//)

# Checking data types
print(type(10))         # int
print(type(3.14))       # float
print(type(2 + 3j))     # complex number
print(type('Shay'))     # string
print(type([1, 2, 3]))  # list
print(type({'name': 'Shay'}))   # dictionary
print(type({9.8, 3.14, 2.7}))   # set
print(type((9.8, 3.14, 2.7)))   # tuple

# tuple = ordered collection of different data types that can NOT be modified once created
